#include <stdio.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (i < n)
	{
		if (str[i] == (unsigned char)c)
        {
            return(str + i);
        }
			i++;
	}
    return NULL;
}

int	main(void)
{
	unsigned char string[] = "String";
	printf("Resould = %p\n", ft_memchr(string, 'r', 5));
}